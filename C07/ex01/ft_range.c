#include <stdlib.h>

int	*ft_range(int min, int max)
{
	int i;
	int *tab;

	if (max <= min)
		return (NULL);
	if (!(tab = (int *)malloc((max - min) * sizeof(int))))
		return (NULL);
	i = 0;
	while (min < max)
	{
		tab[i] = min;
		min++;
		i++;
	}
	return (tab);
}
