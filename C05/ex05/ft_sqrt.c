int	ft_sqrt(int nb)
{
	long i;

	i = 0;
	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	while ((i * i) < (long)nb)
		i++;
	if (i * i == (long)nb)
		return (i);
	return (0);
}
